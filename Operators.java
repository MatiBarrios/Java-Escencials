public class Operators {
    public static void main(String args[]){
        byte var1=3;
        byte var2=5;
        double div;
        //SUM
        System.out.println("Sum "+(var1+var2));
        //subtract
        System.out.println("Subtract "+(var1-var2));
        //multiplication
        System.out.println("Multiplication "+(var2*var1));
        //division
        div=3f/5f;
        System.out.println("Div "+div);
        div=(double)var1/var2;
        System.out.println("Div "+div);
    }
}
